package PracticeFolder.chapter6;

public class SwitchCase {
    public static void main(String[] args) {
    int day = 3;

    switch (day) {
        case 1:
            System.out.print("monday");
            break;

        case 2:
            System.out.print("tuesday");
            break;

        default:
            System.out.print("Invalid");
        }
    }

}
